using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace NeetCodeSolutions
{
    public class SumOfTwoIntegersSolution
    {
        public static int GetSum(int a, int b)
        {
            int[] arr = new int[] {a,b};
            return arr.Sum();
        }
    }
